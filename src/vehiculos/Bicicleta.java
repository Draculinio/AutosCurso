package vehiculos;

public class Bicicleta {
	
	private String marca;
	private String modelo;
	private String color;
	private boolean plegable;
	
	public bicicleta(String marca, String modelo, String color, boolean plegable) {
		this.marca = marca;
		this.modelo = modelo;
		this.color = color;
		this.plegable = plegable;
	}

}
